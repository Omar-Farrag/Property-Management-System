    @Override
    public AttributeCollection getAttributes() {
        AttributeCollection attributes = new AttributeCollection();
        if (currentTab == 0) {
            String floor = floors[Integer.parseInt(newFloorCMB.getSelectedItem().toString())];
            attributes.add(new Attribute(Name.PURPOSE, newPurposeCMB.getSelectedItem().toString(), Table.PROPERTIES));
            String storeClass = newClassCMB.getSelectedItem().toString();
            attributes.add(new Attribute(Name.CLASS, newClassCMB.getSelectedItem().toString(), Table.PROPERTIES));
            attributes.add(new Attribute(Name.NAME, newStoreName.getText(), Table.PROPERTIES));
            attributes.add(new Attribute(Name.SPACE, newSpace.getText(), Table.PROPERTIES));
            attributes.add(new Attribute(Name.ANNUAL_RATE, newAnnual.getText(), Table.PROPERTIES));
            attributes.add(new Attribute(Name.BI_ANNUAL_RATE, newBiAnnual.getText(), Table.PROPERTIES));
            attributes.add(new Attribute(Name.QUARTERLY_RATE, newQuarterly.getText(), Table.PROPERTIES));
            attributes.add(new Attribute(Name.MONTHLY_RATE, newMonthly.getText(), Table.PROPERTIES));

            attributes.add(new Attribute(Name.STORE_NUM, floor + newStoreNumber.getText(), Table.LOCS));
            attributes.add(new Attribute(Name.MALL_NUM, String.valueOf(newStoreTabMall.getMallNum()), Table.LOCS));
        } else {
            attributes.add(new Attribute(Name.NAME, newMallName.getText(), Table.MALLS));
            attributes.add(new Attribute(Name.ADDRESS, newAddress.getText(), Table.MALLS));

        }
        return attributes;
    }

    @Override
    public Filters getFilters() {
        Filters filters = new Filters();

        String floor = floors[(int) newFloorCMB.getSelectedItem()];
        filters.addEqual(new Attribute(Name.NAME, filterMallCMB.getSelectedItem().toString(), Table.MALLS));
        filters.addEqual(new Attribute(Name.STORE_NUM, floor + filterStoreCMB.getSelectedItem().toString(), Table.LOCS));
        return filters;
    }

