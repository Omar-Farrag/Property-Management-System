    private void filterMallCMBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_filterMallCMBActionPerformed
        Object selectedMall = filterMallCMB.getSelectedItem();
        if (selectedMall == null) {
            return;
        }

        String mallName = selectedMall.toString().trim();
        currentStoreTabMall = controller.getMall(mallName);
        newStoreTabMall = currentStoreTabMall;

        populateCurrentFloorCMBs();
        populateNewFloorCMBs();

        filterStoreCMB.removeAllItems();
        stores = controller.getListOfStores(mallName);
        for (Store store : stores) {
            filterStoreCMB.addItem(store.getName());
        }

    }//GEN-LAST:event_filterMallCMBActionPerformed

    private void filterStoreCMBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_filterStoreCMBActionPerformed
        int currentStoreIndex = filterStoreCMB.getSelectedIndex();
        if (stores == null) {
            clearEditFields();
            return;
        }
        if (currentStoreIndex == -1) {
            clearEditFields();
            return;
        }
        currentStore = stores.get(currentStoreIndex);
        currentTab = 0;
        populateFields();
    }//GEN-LAST:event_filterStoreCMBActionPerformed

