    private void populateEditMallCMBs() {
        ArrayList<Mall> malls;

        currentMallCMB.removeAllItems();
        newMallCMB.removeAllItems();

        malls = controller.getListOfMalls();
        for (Mall mall : malls) {
            currentMallCMB.addItem(mall.getName());
            newMallCMB.addItem(mall.getName());

        }

    }

    private void populateFilterMallCMBs() {
        ArrayList<Mall> malls;

        filterMallCMB.removeAllItems();
        mallCMB.removeAllItems();

        malls = controller.getListOfMalls();
        for (Mall mall : malls) {
            filterMallCMB.addItem(mall.getName());
            mallCMB.addItem(mall.getName());
        }
    }

    private void populateCurrentFloorCMBs() {
        int floors = currentStoreTabMall.getNumFloors();

        filterFloorCMB.removeAllItems();
        currentFloorCMB.removeAllItems();

        for (int i = 0; i < floors; i++) {
            filterFloorCMB.addItem(String.valueOf(i));
            currentFloorCMB.addItem(String.valueOf(i));
        }
    }

    private void populateNewFloorCMBs() {
        int floors = newStoreTabMall.getNumFloors();

        newFloorCMB.removeAllItems();

        for (int i = 0; i < floors; i++) {
            newFloorCMB.addItem(String.valueOf(i));
        }
    }

    private void populateGenericCMBs() {
        currentPurposeCMB.removeAllItems();
        newPurposeCMB.removeAllItems();

        currentClassCMB.removeAllItems();
        newClassCMB.removeAllItems();

        ArrayList<String> values;

        values = controller.getClasses();
        for (String classValue : values) {
            currentClassCMB.addItem(classValue);
            newClassCMB.addItem(classValue);

        }

        values = controller.getPurposes();
        for (String purpose : values) {
            currentPurposeCMB.addItem(purpose);
            newPurposeCMB.addItem(purpose);

        }

    }

